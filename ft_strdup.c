#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		slen;
	int		i;

	slen = 0;
	i = 0;
	while (src[slen] != '\0')
		slen++;
	dest = malloc(sizeof(char) * (slen + 1));
	if (!dest)
		return (0);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}